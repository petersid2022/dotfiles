local map = function(mode, keys, func, desc, silent)
  vim.keymap.set(mode, keys, func, { desc = desc, silent = silent })
end

map("", "`", "", nil, false)
map("", "<D-F1>", "", nil, true) -- disable f1 go to help.txt
map("n", "<leader>e", vim.cmd.Ex, "Neovim: Open Netrw", true)
map("t", "<esc>", "<c-\\><c-n>", nil, false)
map("n", "<leader>`", "<cmd>sp<bar>term<cr><c-w>J<cmd>resize10<cr>i", "Neovim: Launch terminal as a split", true)
map("n", "<leader>re", [[:%s/\<<C-r><C-w>\>/<C-r><C-w>/gIc<Left><Left><Left><Left>]], "Regex: Replace word", false)
map("", "<leader>y", [["+y]], "Neovim: Copy to system clipboard", true)
map("", "x", '"_x', nil, true)
map("x", "p", 'p<cmd>let @+=@0<CR><cmd>let @"=@0<CR>', nil, true) -- Don't yank on put
map("n", "<leader>gr", "<cmd>set spelllang=polytonic<cr>", "Neovim: Set spellchecking language to Greek", false)
map("v", "J", ":m '>+1<CR>gv=gv", nil, true)
map("v", "K", ":m '<-2<CR>gv=gv", nil, true)
map("n", "<C-d>", "<C-d>zz", nil, true)
map("n", "<C-u>", "<C-u>zz", nil, true)
map("n", "<leader>q", "<cmd>copen<cr>", "Neovim: Open the Quickfix list", true)
map("n", "<C-n>", "<cmd>cnext<CR>", nil, true)
map("n", "<C-p>", "<cmd>cprev<CR>", nil, true)
map("n", "<C-q>", "<cmd>call setqflist([], 'r')<CR>", nil, true)
map("n", "<C-h>", "<C-W>h", nil, true)
map("n", "<C-j>", "<C-W>j", nil, true)
map("n", "<C-k>", "<C-W>k", nil, true)
map("n", "<C-l>", "<C-W>l", nil, true)
map("n", "<C-z>", "<C-W>5<", nil, true)
map("n", "<C-x>", "<C-W>5>", nil, true)
